 ABSTRACT


 University of Lampung Library utilize information system technology which can be
 accessed manually by visitors to find specific or general informations, but visitors
 tend overlook the general information when using the library. From this aspect,
 Lampung University Library needs an information system to make it possible for the
 visitors to access various public information with just a glance without interacting
 with the information system. Cyber Mediawall technology is a single web page
 application containing public information which is broadcasted in library public
 space, either in the form of video, audio, image, or text information. The software
 designed by using web technologies such as HTML5, CSS3, Bootstrap framework,
 JSON, HTML5 Websocket, PHP, MySQL, and JavaScript as the application builder.
 This application displayed in a screen which can be seen directly by visitors without
 having to access the system first.

 Prototyping is used as the research method by creating a initial mock-up in advance,
 then surveying the consumers to find out the necessity of content library in Cyber
 Mediawall and revising mock-up according to customer needs in order to adhere the
 needs. Based on the User Acceptance test, Cyber Mediawall managed to display the
 content in the form of a promotional video, images slide show, latest books entry,
 Lampung University news, active schedule of the library, and running text of brief
 announcement that answers the needs of the library.
