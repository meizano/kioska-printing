                                       Perpustakaan di Universitas Lampung menggunakan teknologi sistem informasi yang
 diakses secara manual oleh konsumen untuk mengetahui informasi-informasi umum
 maupun khusus yang perlu diketahui oleh konsumen. Kerap konsumen jarang membaca
 informasi-informasi umum yang mungkin perlu diketahui mereka saat menggunakan
 perpustakaan. Dilihat dari aspek ini UPT Perpustakaan Universitas Lampung
 membutuhkan sebuah sistem informasi yang memungkinkan bagi konsumen mengetahui
 berbagai informasi umum hanya dengan sekali melihat tanpa perlu berinteraksi dengan
 sistem informasi tersebut. Teknologi Cyber Mediawall merupakan sebuah single web
 page application yang berisikan informasi-informasi umum yang dapat diinformasikan
 ke setiap orang yang melihatnya, baik dalam bentuk video, audio, gambar, maupun
 informasi text. Perancangan menggunakan teknologi web berupa HTML5, CSS3,
 framework Bootstrap, AJAX JSON, HTML5 Websocket, php, mysql, dan javascript
 sebagai pembangun aplikasi. Aplikasi ini akan ditampilkan dalam sebuah layar yang
 kemudian akan dilihat secara langsung oleh pengunjung tanpa harus mengakses
 sistemnya terlebih dahulu.

 Perancangan Cyber Mediawall ini menggunakan metode prototyping dengan membuat
 mock-up awal terlebih dahulu. Kemudian, melakukan survey kebutuhan konsumen
 perpustakaan untuk mengetahui konten apa saja yang dibutuhkan konsumen pada Cyber
 Mediawall dan merevisi mock-up sesuai dengan kebutuhan konsumen agar menjadi
 aplikasi yang utuh. Berdasarkan User Acceptance Test, Cyber Mediawall berhasil
 menampilkan konten berupa video promosi, slide show gambar promosi, daftar buku
 terbaru, pengumuman seputar Universitas Lampung, jadwal aktif perpustakaan, dan
 running text yang menampilkan pengumuman singkat yang menjawab kebutuhan
 perpustakaan.

